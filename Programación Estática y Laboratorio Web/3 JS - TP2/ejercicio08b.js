function ocultarNeuquen() {
    document.getElementById("neuquenCiudades").style.display = "none";
    document.getElementById("neuquenProvincia").style.textDecoration =  "underline";
}

function mostrarNeuquen() {
    document.getElementById("neuquenCiudades").style.display = "block";
    document.getElementById("neuquenProvincia").style.textDecoration =  "none";
}

